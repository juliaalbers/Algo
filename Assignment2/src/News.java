
public class News {
	String m_title;
	String m_description;
	
	News(){
		
	}
	
	public void setTitle(String title) {
		m_title = title;
	}
	
	public void setDescription(String descpt) {
		m_description = descpt;
	}
	
	public String getTitle() {
		return m_title;
	}
	
	public String getDescription() {
		return m_description;
	}
	
}
